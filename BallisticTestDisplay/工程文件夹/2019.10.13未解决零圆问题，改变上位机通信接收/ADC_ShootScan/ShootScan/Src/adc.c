uint32_t TestCnt = 0;

//DMA传输完成回调函数
void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc)
{
    TestCnt++;
    
    //把数据存在缓冲区里面
    SaveSampleData();
}

uint32_t TrigCnt = 0;
bool isRisingEdge = false;  //标记现在是否是下降沿捕获状态

//模拟看门狗中断回调
void HAL_ADC_LevelOutOfWindowCallback(ADC_HandleTypeDef* hadc)
{
    TrigCnt++;
    
    //上升沿捕获为复位条件
    if(isRisingEdge)
    {
        //更改触发条件为下降沿
        SetTrigValue((Trig_X < Trig_Y ? Trig_X : Trig_Y) - ERROR_BAND, ADC_MAX_VALUE);
        
        isRisingEdge = false;
    }
    else //下降沿捕获为测量条件
    {
        //更改触发条件为上升沿
        SetTrigValue(ADC_MIN_VALUE, (Trig_X > Trig_Y ? Trig_X : Trig_Y) + ERROR_BAND);
                
        //开中断，在中断中向缓冲区中存数
        HAL_NVIC_EnableIRQ(DMA1_Channel1_IRQn);
                
        isRisingEdge = true;
    }
}

